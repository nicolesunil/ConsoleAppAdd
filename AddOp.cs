using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleAddition
{
    internal class AddOp
    {
        public int Add(int a, int b)
        {
            return a + b;
        }

        //Addition of three numbers
        public int Add(int a, int b, int c)
        {
            return a + b + c;
        }
    }
}
